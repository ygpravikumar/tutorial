//Package stringutil definition
//Any name that begins with a capital letter is exported from a package source
package stringutil

//Reverse function to reverse an input string
func Reverse(s string) string {
	runes := []rune(s)
	for i, j := 0, len(runes)-1; i < j; i, j = i+1, j-1 {
		runes[i], runes[j] = runes[j], runes[i]
	}
	return string(runes)
}
